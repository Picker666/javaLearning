/**
 * 内部类
 *      内部类就是定义爱一个类里边的类，内部的类可以理解成寄生，外部类可以理解成宿主。
 *
 *  内部类的使用场景和作用
 *      1、当一个事物的内部，还有一个部分需要一个完整的结构进行描述，而这个内部类的完整结构有只为外部事物提供服务，那么整个内部的完整结构可以选择内部类来设计。
 *      2、内部类通常可以方便访问外部类的成员，包括私有成员
 *      3、内部类提供了更好的封装性，内部类本身就可以使用private protected等修饰，封装可以做更多的控制。
 *
 *  内部类的分类
 *      1、静态内部类
 *      2、成员内部类（非静态）
 *      3、局部内部类
 *      4、匿名内部类
 * */
