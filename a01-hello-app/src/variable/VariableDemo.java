package variable;

public class VariableDemo {
    public static void main(String[] args) {
        double money = 66.66;
        System.out.println(money);

        money = money + 4.9;

        System.out.println(money);

        System.out.println("-------------");

        int age = 90;

        System.out.println(age);

        age = 99;

        System.out.println(age);
    }
}
